
public class Points {

	int local_placement;
	int rounds;
	int num_runners;
	int speed;
	
	int runnerNum;
	
	public Points(int local, int rounds, int speed, int runner, int num_runners) {
		this.local_placement = local;
		this.rounds = rounds;
		this.speed = speed;
		this.runnerNum = runner;
		this.num_runners = num_runners;
	}
	
}